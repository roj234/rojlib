package ilib.util;

import ilib.api.BlockColor;
import net.minecraft.block.state.IBlockState;
import net.minecraft.client.renderer.color.IBlockColor;
import net.minecraft.util.math.BlockPos;
import net.minecraft.world.IBlockAccess;
/**
 * No description provided
 *
 * @author Roj234
 * @version 0.1
 * @since 2021/4/21 22:51
 */
public final class BlockTinter implements IBlockColor {
    @Override
    public int colorMultiplier(IBlockState state, IBlockAccess world, BlockPos pos, int tintIndex) {
        // 在这里返回的颜色代码将决定最终物品模型的“颜色”。这里使用 RGB。
        // 第二个参数即是上文中提到的 `tintIndex` 属性的值。
        return ((BlockColor) state.getBlock()).getColorMultiplier(state, world, pos, tintIndex);
    }
}